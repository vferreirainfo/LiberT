    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:

        jComboBox2.removeAllItems();
        String clavierPrimaireER = "[0-9]{1,10}";
        
        Object obj = evt.getItem();
        String s = obj.toString();
       
        Pattern p = Pattern.compile(clavierPrimaireER);
        Matcher match = p.matcher(s);
        if(match.find())
        {
            int numeroId = Integer.parseInt(match.group());
            idClientFK=numeroId;
            for (int i=0; i<clients.size(); i++)
            {
                if(idClientFK == clients.get(i).ObtenirIdClient())
                {
                    client = clients.get(i);
                }
            }
            
            this.AppelRESTPourObtenirVoitureClient(numeroId);
            
        }
        
    }//GEN-LAST:event_jComboBox1ItemStateChanged

