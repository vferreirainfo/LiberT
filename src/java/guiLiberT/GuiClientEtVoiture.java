    public boolean AppelRESTPourObtenirVoitureClient(int clientId)
    {
        boolean etat=false;
        Voiture voiture = new Voiture();
        GestionMethodesVoiture gestionMethodesVoiture = new GestionMethodesVoiture();
        ServiceVoiture sVoiture = new ServiceVoiture();
        try
        {
            gestionMethodesVoiture= sVoiture.ObtenirTousLesVoitures(clientId);
            if(gestionMethodesVoiture != null)
            {
                for (int i=0; i<gestionMethodesVoiture.ObtenirTousVoitures().size(); )
                {
                    voiture = gestionMethodesVoiture.ObtenirTousVoitures().get(i);
                    voitures.add(voiture);
                    
                    jComboBox2.addItem("Id: "+voiture.ObtenirClavierPrimaireVoiture()+" | Marque: "+voiture.ObtenirMarque()+" | Model: "+voiture.Modelo()+" | Immatriculation: "+voiture.ImmatriculationVoiture());
                    i++;
                }
            }
            
            etat=true;
        }
        catch(Exception ex)
        {
            etat=false;
        }
        return etat;
    }

